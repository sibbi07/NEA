import pygame
from Enemy_file import Enemy, enemy_group
from Enemy_AI import Node

tile_size = 48
ground_level = 466 


class Tilemap():
    def __init__(self, data, window):
        self.tile_list = []
        
        self.grass_img = pygame.image.load("maps/individual tiles/grass block (1).png")
        self.block_2 = pygame.image.load("maps/individual tiles/block_(2).png")
        self.block_3 = pygame.image.load("maps/individual tiles/block_(3).png")
        self.block_4 = pygame.image.load("maps/individual tiles/block_(4).png")

        self.tilemap_height = len(data) * tile_size
        self.tilemap_width = len(data[0]) * tile_size 
        self.ground_offset = self.tilemap_height - tile_size

        self.row_count = 0
        for row in data:
            self.column_count = 0
            for tile in row:
                if tile == 1:
                    self.img = pygame.transform.scale(self.grass_img, (tile_size, tile_size))
                    self.img_rect = self.img.get_rect()
                    self.img_rect.x = tile_size * self.column_count
                    self.img_rect.y = (tile_size * self.row_count) - self.ground_offset + ground_level + 50
                    self.tile = (self.img, self.img_rect)
                    self.tile_list.append(self.tile)
                if tile == 2:
                    self.img = pygame.transform.scale(self.block_2, (tile_size, tile_size))
                    self.img_rect = self.img.get_rect()
                    self.img_rect.x = tile_size * self.column_count
                    self.img_rect.y = (tile_size * self.row_count) - self.ground_offset + ground_level + 50
                    self.tile = (self.img, self.img_rect)
                    self.tile_list.append(self.tile)
                if tile == 3:
                    self.img = pygame.transform.scale(self.block_3, (tile_size, tile_size))
                    self.img_rect = self.img.get_rect()
                    self.img_rect.x = tile_size * self.column_count
                    self.img_rect.y = (tile_size * self.row_count) - self.ground_offset + ground_level + 50
                    self.tile = (self.img, self.img_rect)
                    self.tile_list.append(self.tile)
                if tile == 4:
                    self.img = pygame.transform.scale(self.block_4, (tile_size, tile_size))
                    self.img_rect = self.img.get_rect()
                    self.img_rect.x = tile_size * self.column_count
                    self.img_rect.y = (tile_size * self.row_count) - self.ground_offset + ground_level + 50
                    self.tile = (self.img, self.img_rect)
                    self.tile_list.append(self.tile)

                if tile == 5:
                    enemy = Enemy((tile_size * self.column_count), ((tile_size * self.row_count) - self.ground_offset + ground_level + 50) - 5)
                    enemy_group.add(enemy)
                self.column_count += 1
            self.row_count += 1

    def draw(self, window, camera_x_offset, camera_y_offset):
        for img, img_rect in self.tile_list:
            window.blit(img, img_rect.move(-camera_x_offset, -camera_y_offset))


